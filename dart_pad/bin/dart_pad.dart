void main() {
  // Create a string with some text.
  var text = 'Hello, world! This is a test.';

  // Split the string on the space character.
  var words = text.split(' ');
  var maping = text.toString().split('');

  // Print the list of substrings.
  print(text);
  print(words);
  print(words.length);
  print(maping);
  print(maping.length);
}